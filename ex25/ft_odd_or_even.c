#include <unistd.h>

void	ft_odd_or_even(int n)
{
	if (n % 2 == 0)
		write(1, &"P ", 2);
	else
		write(1, &"I ", 2);
	while (n >= 10)
	{
		write(1, &" ", 1);
		n = n % 10;
	}
}
